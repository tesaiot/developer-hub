/**
 * Debug output levels (ascending verbosity)
 */
#define TESAIOT_DEBUG_LEVEL_NONE    0  /**< No debug output (production minimal) */
#define TESAIOT_DEBUG_LEVEL_ERROR   1  /**< Critical errors only */
#define TESAIOT_DEBUG_LEVEL_WARNING 2  /**< Errors + warnings */
#define TESAIOT_DEBUG_LEVEL_INFO    3  /**< Errors + warnings + informational */
#define TESAIOT_DEBUG_LEVEL_VERBOSE 4  /**< All debug output (development) */

/**
 * Current debug level - Change this to control output verbosity
 */
#ifndef TESAIOT_DEBUG_LEVEL
#define TESAIOT_DEBUG_LEVEL TESAIOT_DEBUG_LEVEL_VERBOSE
#endif

